    public void setMax() {
        int max = 0;
        max = JPEGaBMPyViceversa.isSelected()? max + 1 : max;
        max = CopiaJPEG.isSelected()? max + 1 : max;
        max = RojoVerdeAzulSepia.isSelected()? max + 1 : max;
        max = ModificarImagen.isSelected()? max + 1 : max;
        max = BlancoyNegro.isSelected()? max + 1 : max;
        max *= colaImagenes.getSize();
        CantProcesada.setMaximum(max);
    }

    public void render() {
        DefaultListModel modelo = new DefaultListModel();
        ColaImagen respaldo = new ColaImagen();
        Imagen imagen;
        int repeticion = colaImagenes.getSize();
        for (int i = 0; i < repeticion; i++) {
            imagen = (Imagen) colaImagenes.pop();
            modelo.addElement(imagen.nombre);
            respaldo.add(imagen);
        }
        ColaProcesamiento.setModel(modelo);
        colaImagenes = respaldo;
       
    }

