    private static void funcionEliminarCarpeta1(File pArchivo) {
        if (!pArchivo.exists()) { return; }
        if (pArchivo.isDirectory()) {
            for (File f : pArchivo.listFiles()) {
                funcionEliminarCarpeta1(f);  }
        }
        pArchivo.delete();
    }       
    
