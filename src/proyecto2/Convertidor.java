    private class ItemChangeListener implements ItemListener {

        @Override
        public void itemStateChanged(ItemEvent event) {
            if (event.getSource() == Usuarios) {
                String userName = Usuarios.getSelectedItem().toString();
                JComboBox modeloCategorias = new JComboBox();
                Usuario usuario1 = (Usuario) AppState.usuarios.find(userName);
                for (int i = 0; i < usuario1.categorias.getSize(); i++) {
                    modeloCategorias.addItem(((Categoria) usuario1.categorias.get(i)).nombre);
                }
                Categoria.setModel(modeloCategorias.getModel());
            }
        }
    }
    
